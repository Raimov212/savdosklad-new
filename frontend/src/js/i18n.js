export let currentLang = localStorage.getItem('appLang') || 'uz';

export function setAppLang(lang) {
    localStorage.setItem('appLang', lang);
    currentLang = lang;
    window.currentLang = lang;
    if (typeof translateDOM === 'function') {
        translateDOM();
    }
    if (typeof navigateTo === 'function') {
        navigateTo(window.currentPage || 'dashboard');
    } else {
        window.location.reload();
    }
}

export function t(key) {
    if (!key) return key;
    key = key.trim();
    const lang = window.currentLang || currentLang || 'uz';
    const dict = translations[lang];
    if (dict) {
        if (dict[key]) return dict[key];

        const upper = key.toUpperCase();
        if (dict[upper]) return dict[upper];

        const lower = key.toLowerCase();
        if (dict[lower]) return dict[lower];

        const cap = key.charAt(0).toUpperCase() + key.slice(1).toLowerCase();
        if (dict[cap]) return dict[cap];
    }
    return key;
}

// Function to translate DOM elements automatically
export function translateDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');

        // Page Title special handling
        if (el.tagName === 'TITLE') {
            document.title = t(key);
            return;
        }

        // Meta description special handling
        if (el.tagName === 'META' && el.getAttribute('name') === 'description') {
            el.setAttribute('content', t(key));
            return;
        }

        // Translate title if exists
        if (el.hasAttribute('title')) {
            el.setAttribute('title', t(key));
        }

        // Handle data-i18n-title separately
        const titleKey = el.getAttribute('data-i18n-title');
        if (titleKey) {
            el.setAttribute('title', t(titleKey));
        }

        // For inputs, translate placeholder
        if (el.tagName === 'INPUT' && (el.hasAttribute('placeholder') || el.hasAttribute('data-placeholder'))) {
            el.setAttribute('placeholder', t(key));
        } else {
            // Check if it has an icon inside
            const icon = el.querySelector('.icon') || el.querySelector('i');
            if (icon) {
                const iconClone = icon.cloneNode(true);
                el.innerHTML = '';
                el.appendChild(iconClone);
                el.appendChild(document.createTextNode(' ' + t(key)));
            } else if (el.tagName !== 'INPUT' && el.tagName !== 'TEXTAREA' && el.tagName !== 'SELECT') {
                el.textContent = t(key);
            }
        }
    });
}

window.t = t;
window.setAppLang = setAppLang;
window.translateDOM = translateDOM;
window.currentLang = currentLang;

document.addEventListener('DOMContentLoaded', translateDOM);
